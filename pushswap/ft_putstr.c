#include "push_swap.h"

int	ft_putstr(char *s)
{
	int	i;

	if (s == NULL)
		return (ft_putstr("(null)"));
	i = 0;
	while (s[i] != '\0')
		ft_putchar(s[i++]);
	return (i);
}
