#include "push_swap.h"

int	ft_nbr(int n)
{
	if (n == -2147483648)
		return (ft_putstr("-2147483648"));
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
		return (ft_putnbr_base(n, "0123456789") + 1);
	}
	return (ft_putnbr_base(n, "0123456789"));
}
